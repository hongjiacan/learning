#!/usr/bin/env python3
# -*- coding: utf-8 -*-

# 字符串
str1 = 'hello world'
str2 = "hello 'world'"
str3 = '''hello 
"world"
'''

print(str1)
print(str2)
print(str3)

# 整数
int1 = 1
int2 = -100
int3 = 0
int4 = 1/2

print(int1)
print(int2)
print(int3)
print(int4)

print(type(str1))
print(type(int1))
print(type(int4))


# 浮点数
print(0.55+0.41)
print(0.55+0.4)
print(0.55+0.411)


# 布尔值
print(1==1)
print(1==0)
print(1==1 and 1==0)
print(1==1 or 1==0)
print(not 1==1)
print(not 1==0)


# 空值
print(None)


# 基本数据类型转换
print(int('100'))
print(int(88.88))
